// Componente React descreve um elemento que representa algo 
// que vai ser mostrado para quem usar a aplicação.

// Componente definido via classe, que herda de React.Component, 
// precisa ter definido o método render.

// Componente, da mesma forma que função, pode receber dados de entrada. 
// Esse dado de entrada no contexto de componente se chama props e é um objeto.

// props é somente leitura. props(Propriedades)

// Uma aplicação React é construída via composição de componentes,
// por isso é importante criar componentes da forma mais reutilizável
// e independente possível.

// Quando é preciso criar uma lista de componentes dinamicamente, 
// é preciso passar para cada um dos elementos dessa lista uma key; ou seja, 
// uma chave, de forma que cada um desses elementos consiga ser 
// unicamente identificado pelo React.